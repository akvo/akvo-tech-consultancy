export const splitTitle = (title) => {
    let words = title.split(" ");
    let wordsArr = [];
    for (const w in words) {
        let word = words[w];
        let b = w + 1;
        if (b % 13 == 0) {
            word = word + "\n";
        }
        wordsArr.push(word);
    }
    return wordsArr.join(" ");
};

